package Day03_MatematikselIslemler_Inc;

public class C05_preIncrement {
    public static void main(String[] args) {
        int sayi = 10;
        System.out.println(++sayi);
        System.out.println(sayi--);
        System.out.println(sayi);

        int a=10;
        int b= ++a;
        System.out.println("a: " + a + ", b: " + b);



    }
}
